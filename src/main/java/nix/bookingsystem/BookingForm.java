  private void _bookButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event__bookButtonActionPerformed
  {//GEN-HEADEREND:event__bookButtonActionPerformed
    String name = _nameField.getText();
    String IC = _ICField.getText();
    String phone = _phoneField.getText();
    String email = _emailField.getText();

    _currBooking.SetBookDetail(name, IC, phone, email, BookingSystem.currUser);
    // validation
    String validation = _currBooking.ValidateBookingDetails();
    if (validation != null)
    {
      JOptionPane.showMessageDialog(this, validation);
      return;
    }
    _currBooking.checkout = false;
    BookingSystem.bookingManager.AddBooking(_currBooking);

    // save bookings
    try
    {
      BookingSystem.bookingManager.SaveBookings();
      JOptionPane.showMessageDialog(this, "Data saved successfully!");
      BookingSystem.roomsForm.FilterRooms();
      BookingSystem.SwitchForm(BookingSystem.roomsForm);
    } catch (IOException ex)
    {
      Logger.getLogger(BookingForm.class.getName()).log(Level.SEVERE, null, ex);
      JOptionPane.showMessageDialog(this, "An error occured while saving booking data...");
    }
  }//GEN-LAST:event__bookButtonActionPerformed

