  public void InitializeBookingRoom(Booking booking, Room room)
  {
    // initialize image
    ImageIcon imageIcon = new ImageIcon(room.imageName);
    float width = imageIcon.getIconWidth();
    float height = imageIcon.getIconHeight();
    
    Image image = imageIcon.getImage();
    image = image.getScaledInstance(
      (int)_imageWidth, (int)(_imageWidth/width*height), Image.SCALE_SMOOTH
    );
    imageIcon.setImage(image);
    _roomImage.setIcon(imageIcon);

    // initialize room name
    _roomNameLabel.setText(room.name);

    // initialize date
    _startDateChooser.setDate(booking.startDate);
    _endDateChooser.setDate(booking.endDate);
    
    // set booking
    _currBooking = booking;
    
    // clear fields
    _nameField.setText("");
    _ICField.setText("");
    _phoneField.setText("");
    _emailField.setText("");
  }
  
