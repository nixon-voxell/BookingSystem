  public void InitializeReceipt(Booking booking)
  {
    ArrayList<Room> _rooms = BookingSystem.roomManager.getRooms();
    _nameField.setText(booking.customerName);
    _ICField.setText(booking.IC);
    _phoneField.setText(booking.phoneNumber);
    _emailField.setText(booking.email);
    _startDateChooser.setDate(booking.startDate);
    _endDateChooser.setDate(booking.endDate);
    _roomField.setText(_rooms.get(booking.roomIdx).name);

    int price = booking.endDate.compareTo(booking.startDate)*(350 + 10);
    _priceField.setText("RM " + price);
  }

